<?php
	Bugs::actions(
		'test'
	);
	Router::paths(array(
		'/~{user}'=>function($res,$args){
			Bugs::activity('test','test');
			$res->write(
				Bugs::template('user')
					->run(Bugs::user($args->user))
			);
		},
		'/user/{user}'=>function($res,$args){
			$res->header(
				'Location',
				Router::url(Router::$base.'/~'.$args->user)
			);
		}
	));
?>